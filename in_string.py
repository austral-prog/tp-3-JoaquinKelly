def check_vowels():
    nombre = str(input("Ingresa tu nombre:")).lower()
    a = "a"
    e = "e"
    i = "i"
    o = "o"
    u = "u"
    
    print(f"contiene {a}: {"a" in nombre}")
    print(f"contiene {e}: {"e" in nombre}")
    print(f"contiene {i}: {"i" in nombre}")
    print(f"contiene {o}: {"o" in nombre}")
    print(f"contiene {u}: {"u" in nombre}")
